Ostro.Helpers.KeyboardHelper = {
    isUp: function(keyCode) { return keyCode == 38; },
    isLeft: function(keyCode) { return keyCode == 37; },
    isRight: function(keyCode) { return keyCode == 39; },
    isSpace: function(keyCode) { return keyCode == 32; },

    isW: function(keyCode) { return keyCode == 87;},
    isA: function(keyCode) { return keyCode == 65; },
    isD: function(keyCode) { return keyCode == 68; }
};